export function classifyLawArea(caseNumber: string): LawArea {
  // 사건번호에서 연도(4자리) 뒤의 한글 부호 추출
  const match = caseNumber.match(/\d{4}([가-힣]+)\d/);
  if (!match) return "민사법";

  const code = match[1];

  // 정확한 매칭
  if (code in CODE_TO_LAW_AREA) return CODE_TO_LAW_AREA[code];

  // 긴 접두사부터 짧은 접두사 순으로 시도 (카허 vs 카 등 구분)
  for (let len = code.length - 1; len >= 1; len--) {
    const prefix = code.substring(0, len);
    if (prefix in CODE_TO_LAW_AREA) return CODE_TO_LAW_AREA[prefix];
  }

  // 헌법재판소 fallback (헌XX 등 미등록 코드 대응)
  if (code.startsWith("헌")) return "공법";

  return "민사법";
}
